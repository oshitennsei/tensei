export type Strings = typeof JA;

export function getStrings(lang: UILanguage): Strings {
  if (lang === "zh-tw") return ZH_TW;
  if (lang === "zh-cn") return ZH_CN;
  if (lang === "en") return EN;
  return JA;
}

export function langFromStorage(uiLang: string | undefined): UILanguage {
  if (uiLang === "zh-tw") return "zh-tw";
  if (uiLang === "zh-cn") return "zh-cn";
  if (uiLang === "zh") return "zh-tw";
  if (uiLang === "en") return "en";
  return "ja";
}

export async function loadUILanguage(): Promise<UILanguage> {
  try {
    const { db } = await import("@/lib/storage");
    const s = await db.app_settings.get("global");
    return langFromStorage(s?.ui_language);
  } catch { return "ja"; }
}

export function useStrings(): Strings {
  const [strings, setStrings] = useState<Strings>(JA);
  useEffect(() => {
    loadUILanguage().then(lang => {
      if (lang !== "ja") setStrings(getStrings(lang));
    });
  }, []);
  return strings;
}

export function useLang(): UILanguage {
  const [lang, setLang] = useState<UILanguage>("ja");
  useEffect(() => { loadUILanguage().then(setLang); }, []);
  return lang;
}
